#include "../libft.h"

int	ft_lstsize(t_list *lst)
{
	size_t	i;
	t_list	*ptr;

	i = 0;
	ptr = lst;
	while (ptr)
	{
		ptr = ptr->next;
		i++;
	}
	return (i);
}
