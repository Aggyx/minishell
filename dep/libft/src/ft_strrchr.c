#include "../libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*ret;
	int		i;

	ret = NULL;
	i = 0;
	while (s[i])
	{
		if ((unsigned char)s[i] == (unsigned char)c)
			ret = &((char *)s)[i];
		i++;
	}
	if ((unsigned char)c == 0)
		ret = &((char *)s)[i];
	return (ret);
}
