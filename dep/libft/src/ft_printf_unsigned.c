#include "../libft.h"

int	ft_printf_unsigned(unsigned int num)
{
	int		rtn;

	rtn = 0;
	if (num == 0)
		rtn += ft_putchar('0');
	else
		rtn += ft_utoa(num);
	return (rtn);
}
